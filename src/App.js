// ─── FLATTEN для поиска ───────────────────────────────────────────────────────
function flatten(node, acc = []) {
  acc.push(node);
  node.children?.forEach(c => flatten(c, acc));
  return acc;
}
const ALL_NODES = flatten(TREE);

// ─── LEVEL COLORS ─────────────────────────────────────────────────────────────
const LC = ["#d4af37","#60a5fa","#a78bfa","#34d399","#f97316","#94a3b8"];
const lc = l => LC[Math.min(l, LC.length-1)];

function getLevel(node, tree, lvl=0) {
  if (tree.id === node.id) return lvl;
  for (const c of (tree.children||[])) {
    const r = getLevel(node, c, lvl+1);
    if (r !== -1) return r;
  }
  return -1;
}

// ─── TREE NODE ────────────────────────────────────────────────────────────────
function TreeNode({ node, level, expandedSet, onToggle, onSelect, selectedId, searchIds }) {
  const isExpanded = expandedSet.has(node.id);
  const isSelected = selectedId === node.id;
  const hasChildren = node.children?.length > 0;
  const isHighlighted = searchIds?.size > 0 && searchIds.has(node.id);

  return (
    <div style={{ marginLeft: level * 16 }}>
      <div
        onClick={() => { onSelect(node); if (hasChildren) onToggle(node.id); }}
        style={{
          display: "flex", alignItems: "center", gap: 8,
          padding: "7px 10px", marginBottom: 2, borderRadius: 7, cursor: "pointer",
          background: isSelected ? `${lc(level)}15` : isHighlighted ? "#d4af3715" : "transparent",
          border: `1px solid ${isSelected ? lc(level) : isHighlighted ? "#d4af3766" : "transparent"}`,
          transition: "all 0.12s",
        }}
      >
        <div style={{ width: 16, flexShrink: 0, color: "#4b5563", fontSize: 10, textAlign: "center" }}>
          {hasChildren ? (isExpanded ? "▼" : "▶") : "·"}
        </div>
        <div style={{ width: 7, height: 7, borderRadius: "50%", background: lc(level), flexShrink: 0 }} />
        <div style={{ flex: 1, minWidth: 0 }}>
          <div style={{ fontSize: 12.5, fontWeight: node.birthday ? 600 : 400, color: isSelected ? lc(level) : "#e5e7eb",
            whiteSpace: "nowrap", overflow: "hidden", textOverflow: "ellipsis" }}>
            {node.name}
          </div>
          <div style={{ fontSize: 10, color: "#6b7280", whiteSpace: "nowrap", overflow: "hidden", textOverflow: "ellipsis" }}>
            {node.title}
          </div>
        </div>
        {node.since && <div style={{ fontSize: 9, color: "#374151", flexShrink: 0 }}>{node.since}</div>}
      </div>

      {hasChildren && isExpanded && (
        <div style={{ borderLeft: `1px solid ${lc(level)}22`, marginLeft: 16, paddingLeft: 4 }}>
          {node.children.map(child => (
            <TreeNode key={child.id} node={child} level={level + 1}
              expandedSet={expandedSet} onToggle={onToggle}
              onSelect={onSelect} selectedId={selectedId} searchIds={searchIds} />
          ))}
        </div>
      )}
    </div>
  );
}

